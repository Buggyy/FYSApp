    /**
     *
     * @param evt
     * @description all data of the filled in fields will be saved into the
     * database and will receive the status 'Lost'
     */
    private void btn_submit1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_submit1ActionPerformed

        //  Create client
        String firstName = txt_firstName.getText();
        String middleName = txt_middleName.getText();
        String lastName = txt_lastName.getText();

        String phone = txt_phoneNumber.getText();
        String email = txt_email.getText();

        String country = txt_country.getText();
        String address = txt_address.getText();
        String city = txt_city.getText();
        String state = txt_state.getText();
        String zipCode = txt_zipCode.getText();

        //  Calling client constructor from the client class
        Client newClient = new Client(firstName, middleName, lastName, phone,
                email, country, address, city, state, zipCode);

        //  store user input in variables
        String brand = txt_brand.getText();
        String color
                = String.valueOf(cmb_color.getSelectedItem());
        String weightClass
                = String.valueOf(cmb_weightClass.getSelectedItem());
        String departureFrom
                = String.valueOf(cmb_departureFrom.getSelectedItem());
        String material = txt_material.getText();
        String lableCode = txt_lableCode.getText();
        String otherDetails = txt_otherDetails.getText();
        String status = "Lost";
        String whenFound = "";
        String foundAt = "";

        //  Create lost luggage item with user input
        Luggage luggage = new Luggage(brand, lableCode, material,
                otherDetails, status, color, weightClass, whenFound, foundAt,
                departureFrom);

        if (checkPDF.isSelected()) {
            FYSApp.getInstance().getPDFGenerator().createPDF(luggage, newClient);
        }  
        if (updateMode > 1) {
            FYSApp.getLuggageManager().updateLuggage(luggage, luggageid);
            try {
                Frame.getInstance().showPanel(new LostLuggageOverview());
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(RegisterLostLuggage.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                Frame.getInstance().showPanel(new LostLuggageOverview());
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(RegisterLostLuggage.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            try {
                if (checkIfExists(luggage)) {

                    ResultSet rs = FYSApp.getSearchManager().searchSimilarities(luggage, false);
                    JOptionPane.showMessageDialog(null, "Possible match found!");
                    JOptionPane.showMessageDialog(null, "You will now be redirected to the overview of the matched luggage");
                    Frame.getInstance().showPanel(new MatchingLuggage());
                    MatchingLuggage.getMatchingLuggage(rs);
                    MatchingLuggage.setLostOrFound(status);
                    MatchingLuggage.setTextLuggage(luggage);
                    MatchingLuggage.setTextClient(newClient);

                } else {
                    //  Show user succes message
                    JOptionPane.showMessageDialog(null, "Information is saved");
                    //  If this is not the case, then we call the addLuggage Query +
                    //  the add client luggage
                    FYSApp.getClientManager().addClient(newClient);
                    int id = FYSApp.getClientManager().getClientid();
                    FYSApp.getLuggageManager().addLostLuggage(luggage, id);
                    Frame.getInstance().showPanel(new LostLuggageOverview());
                }

            } catch (HeadlessException | ClassNotFoundException | SQLException e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }


    }//GEN-LAST:event_btn_submit1ActionPerformed
    /**
     *
     * @param lug
     * @return list containing luggage
     * @description makes an arraylist filled with lost luggage and returns the
     * arraylist
     */
    private boolean checkIfExists(Luggage lug) {
        ArrayList<Luggage> list = FYSApp.getLuggageManager().getLostLuggage();
        return list.contains(lug);
    }

